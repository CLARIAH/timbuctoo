  //I think that a fully reversable shortened version looks ugly. And usually this is not needed
  //So I shorten by throwing away information and use a HashMap to be able to revert the process
  //and prevent collisions.
  @Override
  public String makeGraphQlname(String uri) {
    if (data.shorteneds.containsKey(uri)) {
      return data.shorteneds.get(uri);
    } else {
      String shortened = shorten(uri).replaceAll("[^_0-9A-Za-z]", "_");
      while (data.shorteneds.containsKey(shortened)) {
        shortened += "_";
      }
      data.shorteneds.put(uri, shortened);
      data.inverse.put(shortened, uri);
      return shortened;
    }
  }

  @Override
  public String makeUri(String graphQlName) {
    return data.inverse.getOrDefault(graphQlName, graphQlName);
  }

  @Override
  public String shorten(String uri) {
    return prefixMapping.shortForm(uri);
  }

  @Override
  public Map<String, String> getMappings() {
    return prefixMapping.getNsPrefixMap();
  }

  @Override
  public void addPrefix(String prefix, String iri) {
    data.prefixes.put(prefix, iri);
    prefixMapping.setNsPrefix(prefix, iri); //idempotent
  }

  @Override
  public void close() throws IOException {
    save();
  }

  private void save() throws IOException {
    mapper.writeValue(typeNameFile, data);
  }

  @Override
  public void process(QuadLoader source, long version) throws ProcessingFailedException {
    try {
      source.sendQuads(new QuadHandler() {
        @Override
        public void onPrefix(long line, String prefix, String iri) throws LogProcessingFailedException {
          if (data.status.getPosition() > line) {
            return;
          }
          data.status.setPosition(line);
          addPrefix(prefix, iri);
        }

        @Override
        public void finish() throws LogProcessingFailedException {
          data.status.finishUpdate(version);
          try {
            save();
          } catch (IOException e) {
            LOG.error("saving failed", e);
          }
        }

        @Override
        public void start(long lineCount) throws LogProcessingFailedException {
          data.status.startUpdate(lineCount);
        }

        @Override
        public void onRelation(long line, String subject, String predicate, String object, String graph)
            throws LogProcessingFailedException {
        }

        @Override
        public void onLiteral(long line, String subject, String predicate, String object, String valueType,
                              String graph)
            throws LogProcessingFailedException {
        }

        @Override
        public void onLanguageTaggedString(long line, String subject, String predicate, String value, String language,
                                           String graph)
            throws LogProcessingFailedException {
        }

        @Override
        public void cancel() throws LogProcessingFailedException {
        }
      });
    } catch (LogProcessingFailedException e) {
      LOG.error("Storing prefixes failed", e);
      data.status.abortUpdate(e.getCause().getMessage());
      try {
        save();
      } catch (IOException e1) {
        LOG.error("saving failed", e1);
      }
      throw new ProcessingFailedException(e.getCause());
    }
  }

  @Override
  public StoreStatus getStatus() {
    return data.status;
  }

