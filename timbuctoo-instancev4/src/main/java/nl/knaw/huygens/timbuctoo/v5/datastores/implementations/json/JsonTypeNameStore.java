  private final PrefixMapping prefixMapping;
  private final JsonFileBackedData<TypeNames> store;
  private final TypeNames data;

  public JsonTypeNameStore(File dataLocation, DataProvider dataProvider) throws IOException {
    prefixMapping = new PrefixMappingImpl();
    store = JsonFileBackedData.getOrCreate(
      dataLocation,
      () -> {
        TypeNames typeNames = new TypeNames();
        addStandardPrefixes(typeNames);
        return typeNames;
      },
      new TypeReference<TypeNames>() {}
    );
    data = store.getData();
    prefixMapping.setNsPrefixes(data.prefixes);

    dataProvider.subscribeToRdf(new Subscription(), null);
  }

  //I think that a fully reversable shortened version looks ugly. And usually this is not needed
  //So I shorten by throwing away information and use a HashMap to be able to revert the process
  //and prevent collisions.
  @Override
  public String makeGraphQlname(String uri) {
    //The relay spec requires that our own names are never 'PageInfo' or end with 'Connection'

    Map<String, String> shorteneds = data.shorteneds;
    if (shorteneds.containsKey(uri)) {
      return shorteneds.get(uri);
    } else {
      String shortened = shorten(uri).replaceAll("[^_0-9A-Za-z]", "_");
      while (shortened.equals("PageInfo") ||
        shortened.endsWith("Connection") ||
        shortened.endsWith("Edge") ||
        shorteneds.containsKey(shortened)) {

        shortened += "_";
      }
      shorteneds.put(uri, shortened);
      String dataToSave = shortened;
      data.inverse.put(dataToSave, uri);
      return shortened;
    }
  }

  @Override
  public String makeUri(String graphQlName) {
    return store.getData().inverse.getOrDefault(graphQlName, graphQlName);
  }

  @Override
  public String shorten(String uri) {
    return prefixMapping.shortForm(uri);
  }

  @Override
  public Map<String, String> getMappings() {
    return prefixMapping.getNsPrefixMap();
  }

  private void addPrefix(TypeNames typeNames, String prefix, String iri) {
    typeNames.prefixes.put(prefix, iri);
    prefixMapping.setNsPrefix(prefix, iri); //idempotent
  }

  @Override
  public void close() throws IOException {
    store.updateData(old -> data);
  }

  private class Subscription implements RdfProcessor {

    @Override
    public void setPrefix(String cursor, String prefix, String iri) throws RdfProcessingFailedException {
      addPrefix(data, prefix, iri);
    }

    @Override
    public void addRelation(String cursor, String subject, String predicate, String object, String graph)
      throws RdfProcessingFailedException {}

    @Override
    public void addValue(String cursor, String subject, String predicate, String value, String dataType, String graph)
      throws RdfProcessingFailedException {}

    @Override
    public void addLanguageTaggedString(String cursor, String subject, String predicate, String value, String language,
                                        String graph) throws RdfProcessingFailedException {}

    @Override
    public void delRelation(String cursor, String subject, String predicate, String object, String graph)
      throws RdfProcessingFailedException {}

    @Override
    public void delValue(String cursor, String subject, String predicate, String value, String valueType, String graph)
      throws RdfProcessingFailedException {}

    @Override
    public void delLanguageTaggedString(String cursor, String subject, String predicate, String value, String language,
                                        String graph) throws RdfProcessingFailedException {}

    @Override
    public void start() throws RdfProcessingFailedException {

    }

    @Override
    public void finish() throws RdfProcessingFailedException {
      try {
        store.updateData(old -> data);
      } catch (IOException e) {
        throw new RdfProcessingFailedException(e);
      }
    }
  }

